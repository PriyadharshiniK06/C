//Multiplication without symbol in total
#include<stdio.h>
int main()
{
	int value,range,n,c;
	printf("Enter any integer:");
	scanf("%d",&value);
	printf("Enter any range:");
	scanf("%d",&range);	
	for(n=1;n<=range;n++)
	{
		c=c+value;
        }
        c=c-1;
	printf("%d\n",c);
	return 0;
}
